// Positional layout mirrors the compact Python LanguageConfig declarations and
// keeps each grammar's AST contract readable as one table row.
#[allow(clippy::too_many_arguments)]
const fn config(
    class_types: &'static [&'static str],
    function_types: &'static [&'static str],
    import_types: &'static [&'static str],
    call_types: &'static [&'static str],
    call_function_field: &'static str,
    accessor_types: &'static [&'static str],
    accessor_name_field: &'static str,
    accessor_object_field: &'static str,
    name_fallbacks: &'static [&'static str],
) -> GenericConfig {
    GenericConfig {
        class_types,
        function_types,
        import_types,
        call_types,
        call_function_field,
        accessor_types,
        accessor_name_field,
        accessor_object_field,
        name_fallbacks,
        function_boundaries: function_types,
    }
}
